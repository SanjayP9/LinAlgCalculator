public class Tuple {

    private int count;
    private int remainder;

    public Tuple(int count, int remainder) {
        this.count = count;
        this.remainder = remainder;
    }

    public int getCount() {
        return count;
    }

    public int getRemainder() {
        return remainder;
    }
}
